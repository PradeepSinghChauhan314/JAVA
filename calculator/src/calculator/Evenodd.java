package calculator;
import java.util.*;
public class Evenodd {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter a number");
		int X=sc.nextInt();
		if(X%2==0)
		{System.out.println("X is a even number");
			
		}
		else
		{
			System.out.println("X is a odd number");
		}
	}

}
